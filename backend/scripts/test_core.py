async def save_to_prediction_log(
    simulation_id: str,
    ticker: str,
    direction: str,
    confidence: float,
    primary_reason: str,
    market_ctx: Dict[str, Any],
) -> None:
    """Save prediction to prediction_log table (non-blocking)."""
    try:
        from database import get_db, init_db
        await init_db()
        async with get_db() as db:
            await db.execute(
                """INSERT OR REPLACE INTO prediction_log
                   (id, ticker, predicted_direction, confidence, predicted_at,
                    primary_reason,
                    iron_ore_at_prediction, audusd_at_prediction,
                    brent_at_prediction, bhp_price_at_prediction)
                   VALUES (?,?,?,?,?,?,?,?,?,?)""",
                (
                    simulation_id, ticker, direction.lower(),
                    confidence,
                    datetime.now(timezone.utc).isoformat(),
                    primary_reason[:500],
                    market_ctx.get("iron_ore_price"),
                    market_ctx.get("audusd_rate"),
                    market_ctx.get("brent_price"),
                    market_ctx.get("ticker_price"),
                )
            )
            await db.commit()
        logger.info("Saved %s to prediction_log", simulation_id)
    except Exception as e:
        logger.warning("prediction_log save failed (non-critical): %s", e)


