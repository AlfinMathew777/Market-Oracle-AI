# ── Bug Fix 2: Trigger event validation ───────────────────────────────────────

def validate_trigger_event(trigger_event: str, ticker: str, news_items: list) -> str:
    """
    Ensures agents always receive a trigger event context.
    Falls back through 3 levels before giving up.
    """
    # Level 1: Use existing trigger if valid
    if trigger_event and trigger_event.strip() and "No data" not in trigger_event:
        return trigger_event

    # Level 2: Build trigger from raw news items (last 24h only)
    if news_items:
        recent = [n for n in news_items if n.get("hours_old", 999) <= 24]
        if recent:
            top = sorted(recent, key=lambda x: x.get("weight", 0), reverse=True)[0]
            headline = top.get("title", top.get("headline", "Market event"))
            source   = top.get("source", "MarketAux")
            return f"{headline} (Source: {source}, {top.get('hours_old', '?')}h ago)"

    # Level 3: No news — macro/technical-driven session
    return f"No major catalyst identified for {ticker} in last 24h — likely macro/technical-driven session"


# ── Bug Fix 4: Smart direction determination ───────────────────────────────────

def determine_direction(
    bullish: int,
    bearish: int,
    neutral: int,
    volume_vs_avg: Optional[float],
    trigger_event: str,
    confidence: float,
) -> str:
    """
    Determines final direction with commodity-stock bias correction.
    Pure neutral is only returned when conditions genuinely support it.
    Returns 'bullish' | 'bearish' | 'neutral'.
    """
    total        = bullish + bearish + neutral
    neutral_ratio = neutral / total if total > 0 else 0
    no_catalyst  = trigger_event.startswith("No major catalyst")

    # Clear majority (margin >= 5)
    if bullish > bearish and (bullish - bearish) >= 5:
        return "bullish"
    if bearish > bullish and (bearish - bullish) >= 5:
        return "bearish"

    # Near-split on high-volume day with no clear winner = institutional distribution → bearish
    if volume_vs_avg is not None and volume_vs_avg >= 2.0 and neutral_ratio > 0.4:
        return "bearish"

    # Genuine neutral: no catalyst + low volume
    if no_catalyst and (volume_vs_avg is None or volume_vs_avg < 1.3):
        return "neutral"

    # Tiebreaker: asymmetric commodity risk — commodities fall faster than they rise
    if bullish == bearish:
        return "bearish"

    return "bullish" if bullish > bearish else "bearish"


# ── Fix 1: Minimum confidence guard ────────────────────────────────────────────

def apply_minimum_confidence_guard(
    direction: str,
    confidence: float,
    bullish: int,
    bearish: int,
    neutral: int,
) -> Tuple[str, float, Optional[str]]:
    """
    Prevents directional predictions when confidence is too low to be meaningful.
    confidence must be in 0–100 percentage scale.
    direction must be 'bullish' | 'bearish' | 'neutral'.
    Returns (direction, confidence, signal_note_or_None).
    """
    total         = bullish + bearish + neutral
    neutral_ratio = neutral / total if total > 0 else 1.0

    # Rule 1: Hard zero confidence — no direction possible
    if confidence < 3.0:
        return (
            "neutral",
            confidence,
            "INSUFFICIENT_SIGNAL: confidence below minimum threshold — "
            "no directional prediction generated",
        )

    # Rule 2: Majority neutral — market direction genuinely unclear
    if neutral_ratio >= 0.50:
        return (
            "neutral",
            confidence,
            "INSUFFICIENT_SIGNAL: majority of agents returned neutral — "
            "market direction unclear",
        )

    # Rule 3: Near-tie at low confidence — margin too thin to call
    margin = abs(bullish - bearish)
    if margin <= 2 and confidence < 15.0:
        return (
            "neutral",
            confidence,
            f"INSUFFICIENT_SIGNAL: agent margin too thin ({margin} agents) "
            f"at low confidence ({confidence:.1f}%) — defaulting to neutral",
        )

    return direction, confidence, None


# ── Fix 2: Causal chain audit ───────────────────────────────────────────────────

_BEARISH_KEYWORDS = frozenset([
    # Unambiguously negative — count in all trend contexts
    "negative", "decrease", "decline", "drop", "risk", "disruption", "lower",
    "weigh", "sell-off", "selloff", "compressed", "hurt", "pressure", "headwind",
    "contraction", "slowdown", "ban", "sanction", "tariff", "loss", "deficit",
    "shortage", "bearish", "downward", "fall", "weak", "cut", "down",
])

# Uncertainty/vagueness words: the bull case has NOT been proven.
# Count as BEARISH only in downtrends, BULLISH only in uptrends, NEUTRAL otherwise.
_UNCERTAINTY_KEYWORDS = frozenset([
    "unclear", "uncertain", "mixed", "difficult", "limited", "remains",
    "potential", "may", "could", "might", "possible", "suggests", "perhaps",
    "visibility", "ambiguous", "inconclusive",
])

_BULLISH_KEYWORDS = frozenset([
    # Measurable bullish signals only — no vague positives
    "increase", "positive", "rise", "boost", "gain", "higher", "benefit",
    "growth", "accumulation", "strong", "demand", "recovery", "stimulus",
    "upgrade", "expansion", "bullish", "upward", "rally", "surge",
    "outperform", "improve", "rebound", "support", "tailwind", "opportunity", "up",
])

_DOWNTREND_LABELS = frozenset(["STRONG_DOWNTREND", "DOWNTREND"])
_UPTREND_LABELS   = frozenset(["STRONG_UPTREND", "UPTREND"])


def audit_causal_chain(
    judge_result: Dict[str, Any],
    trend_label: str = "NEUTRAL",
) -> Tuple[str, int, int, Dict[str, str]]:
    """
    Counts bearish vs bullish signals in the 4 causal chain slots.
    If ≥3 of 4 slots point the same direction, that direction is the chain verdict.

    Trend-aware behaviour:
      - Empty slots: in a downtrend count as bearish (bull case unproven), else neutral
      - Tied scores:  in a downtrend count as bearish, in an uptrend as bullish, else neutral
      - Uncertainty words ("unclear", "may", "suggests"…) in bearish keyword list — in a
        downtrend they count against the bull case

    Returns (chain_verdict, bearish_slot_count, bullish_slot_count, slot_verdicts_dict).
    """
    is_downtrend = trend_label in _DOWNTREND_LABELS
    is_uptrend   = trend_label in _UPTREND_LABELS

    slots = {
        "cost":      judge_result.get("cost_impact", ""),
        "revenue":   judge_result.get("revenue_impact", ""),
        "demand":    judge_result.get("demand_signal", ""),
        "sentiment": judge_result.get("sentiment_signal", ""),
    }

    bearish_slots = 0
    bullish_slots = 0
    slot_verdicts: Dict[str, str] = {}

    for slot_name, slot_text in slots.items():
        if not slot_text or len(slot_text.strip()) < 10 or "No data" in slot_text:
            # Empty slot — in downtrend counts as bearish (bull case unproven)
            if is_downtrend:
                bearish_slots += 1
                slot_verdicts[slot_name] = "bearish (empty=downtrend default)"
            elif is_uptrend:
                bullish_slots += 1
                slot_verdicts[slot_name] = "bullish (empty=uptrend default)"
            else:
                slot_verdicts[slot_name] = "neutral (empty)"
            continue

        # Word-level tokenisation — prevents "up" hitting inside "disruptions" etc.
        words = [w.rstrip(".,;:!?") for w in slot_text.lower().split()]
        b_hits   = sum(1 for w in words if w in _BEARISH_KEYWORDS)
        u_hits   = sum(1 for w in words if w in _BULLISH_KEYWORDS)
        unc_hits = sum(1 for w in words if w in _UNCERTAINTY_KEYWORDS)

        # Uncertainty keywords: only shift the score in trending markets.
        # An unproven bull case in a downtrend counts as bearish; vice-versa in uptrend.
        if unc_hits > 0:
            if is_downtrend:
                b_hits += unc_hits
            elif is_uptrend:
                u_hits += unc_hits
            # NEUTRAL: uncertainty is ignored — neither side gets credit

        if b_hits > u_hits:
            bearish_slots += 1
            slot_verdicts[slot_name] = f"bearish (b={b_hits}, u={u_hits})"
        elif u_hits > b_hits:
            bullish_slots += 1
            slot_verdicts[slot_name] = f"bullish (b={b_hits}, u={u_hits})"
        else:
            # Tie — default direction based on trend
            if is_downtrend:
                bearish_slots += 1
                slot_verdicts[slot_name] = f"bearish (tie={b_hits}=downtrend default)"
            elif is_uptrend:
                bullish_slots += 1
                slot_verdicts[slot_name] = f"bullish (tie={u_hits}=uptrend default)"
            else:
                slot_verdicts[slot_name] = f"neutral (tied b={b_hits} u={u_hits})"

    if bearish_slots >= 3:
        chain_verdict = "bearish"
    elif bullish_slots >= 3:
        chain_verdict = "bullish"
    else:
        chain_verdict = "neutral"

    return chain_verdict, bearish_slots, bullish_slots, slot_verdicts


