class AdversarialAgent:
    """Single adversarial agent with a locked persona."""

    def __init__(self, agent_id: int, persona: str, system_prompt: str):
        self.agent_id    = agent_id
        self.persona     = persona
        self.system_prompt = system_prompt

    async def vote(
        self,
        llm_router: LLMRouter,
        event_context: str,
        market_context_block: str,
    ) -> Dict[str, Any]:
        """Return a vote dict: {agent_id, persona, vote, reason}."""

        user_prompt = (
            f"{market_context_block}\n\n"
            f"=== GEOPOLITICAL EVENT ===\n{event_context}\n=== END EVENT ===\n\n"
            f"Now give your vote and one-sentence reason as instructed."
        )

        try:
            # Tiered routing: quant/neutral_fund use fast 8b model (3x faster, separate quota).
            # macro_bull/geo_bear require deeper reasoning → use 70b.
            _call = (
                llm_router.call_fast
                if self.persona in ("quant", "neutral_fund")
                else llm_router.call_boost
            )
            response = await _call(
                system_message=self.system_prompt,
                user_prompt=user_prompt,
                session_id=f"agent_{self.agent_id}_{self.persona}",
            )
            vote, reason = _parse_vote(response, self.persona)
        except asyncio.TimeoutError:
            agent_label = f"{self.persona} (Agent {self.agent_id})"
            logger.warning("%s: Response timeout — skipped", agent_label)
            if self.persona == "macro_bull":
                vote = "bullish"
            elif self.persona == "geo_bear":
                vote = "bearish"
            else:
                vote = "neutral"
            reason = "Response timeout — skipped"
        except Exception as e:
            logger.warning("Agent %d (%s) error: %s", self.agent_id, self.persona, e)
            # Forced-vote personas (macro_bull, geo_bear) must not silently become
            # neutral on error — that inflates neutral count and distorts confidence.
            if self.persona == "macro_bull":
                vote = "bullish"
            elif self.persona == "geo_bear":
                vote = "bearish"
            else:
                vote = "neutral"
            reason = f"Error — defaulting to {vote}"

        return {"agent_id": self.agent_id, "persona": self.persona, "vote": vote, "reason": reason}


def _parse_vote(response: str, persona: str) -> Tuple[str, str]:
    """Extract vote and reason from agent response."""
    # Macro bull can only be bullish; geo bear can only be bearish
    if persona == "macro_bull":
        forced_vote = "bullish"
    elif persona == "geo_bear":
        forced_vote = "bearish"
    else:
        forced_vote = None

    lower = response.lower()

    # Parse VOTE: field
    vote = None
    if "vote:" in lower:
        after = lower.split("vote:", 1)[1].strip()
        for candidate in ("bullish", "bearish", "neutral"):
            if after.startswith(candidate):
                vote = candidate
                break
        if vote is None:
            for candidate in ("bullish", "bearish", "neutral"):
                if candidate in after[:30]:
                    vote = candidate
                    break

    if forced_vote:
        vote = forced_vote
    elif vote is None:
        if "bullish" in lower[:100] or "up" in lower[:60]:
            vote = "bullish"
        elif "bearish" in lower[:100] or "down" in lower[:60]:
            vote = "bearish"
        else:
            vote = "neutral"

    # Parse REASON: field
    reason = ""
    if "reason:" in lower:
        parts = response.split(":", 1)
        if len(parts) > 1:
            # find the REASON: part
            idx = response.upper().find("REASON:")
            if idx != -1:
                reason = response[idx + 7:].strip().split("\n")[0].strip()[:300]
    if not reason:
        reason = response.strip()[:200]

    return vote, reason


