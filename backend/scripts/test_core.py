def _fallback_judge_result(
    n_bull: int,
    n_bear: int,
    n_neut: int,
    ticker: str = "",
    event_headline: str = "",
    market_session: str = "UNKNOWN",
) -> Dict[str, Any]:
    """
    Emergency fallback when Phase 7 Judge/Reconciler calls time out or exhaust
    all providers. Derives verdict from raw agent vote counts and generates
    meaningful (if basic) causal chain content from the vote distribution.

    With per-call timeouts inside _run_judge (25s blind + 30s reconciler),
    this should only fire on complete network failure — not just slow LLMs.
    """
    total = n_bull + n_bear + n_neut or 1
    majority = "bullish" if n_bull > n_bear else ("bearish" if n_bear > n_bull else "neutral")

    bull_pct = round(100 * n_bull / total)
    bear_pct = round(100 * n_bear / total)
    neut_pct = round(100 * n_neut / total)

    margin = abs(n_bull - n_bear) / total
    margin_label = "strong" if margin >= 0.30 else ("moderate" if margin >= 0.15 else "thin")

    ticker_ref = f" for {ticker}" if ticker else ""
    headline_ref = f" — {event_headline[:80]}" if event_headline and event_headline != "None" else ""

    dir_adj = {"bullish": "bullish", "bearish": "bearish", "neutral": "mixed/neutral"}[majority]
    cost_adj = "manageable cost environment" if majority == "bullish" else (
        "elevated cost pressure" if majority == "bearish" else "stable cost environment"
    )
    rev_adj = "improving revenue outlook" if majority == "bullish" else (
        "revenue headwinds" if majority == "bearish" else "flat revenue outlook"
    )
    demand_adj = "supportive demand conditions" if majority == "bullish" else (
        "weakening demand" if majority == "bearish" else "mixed demand signals"
    )

    return {
        "verdict":             majority,
        "confidence_modifier": 0,
        "override_flag":       "PHASE7_TIMEOUT",
        "reasoning":           (
            f"Causal synthesis timed out — verdict derived from agent consensus{ticker_ref}: "
            f"{n_bull} bullish / {n_bear} bearish / {n_neut} neutral ({margin_label} {dir_adj} lean)."
        ),
        "trigger_event": (
            f"Agent consensus{ticker_ref} indicates {dir_adj} outlook "
            f"({bull_pct}% bullish, {bear_pct}% bearish, {neut_pct}% neutral){headline_ref}. "
            f"Causal synthesis timed out — vote-based assessment used."
        ),
        "cost_impact": (
            f"{bull_pct}% of agents signalled {cost_adj} "
            f"({n_bull}B / {n_bear}Be / {n_neut}N agents, session={market_session}). "
            f"Detailed cost analysis unavailable — synthesis timeout."
        ),
        "revenue_impact": (
            f"Agent majority ({margin_label} {dir_adj}) implies {rev_adj}. "
            f"{n_bull} agents bullish vs {n_bear} bearish on revenue conditions. "
            f"Reconciler timed out — proxy from vote distribution used."
        ),
        "demand_signal": (
            f"Demand outlook: {dir_adj} consensus ({bull_pct}% bullish, {bear_pct}% bearish). "
            f"{demand_adj} implied by {majority} agent majority. Full demand analysis timed out."
        ),
        "sentiment_signal": (
            f"Market sentiment: {margin_label} {dir_adj} lean — {n_bull}B / {n_bear}Be / {n_neut}N "
            f"agents across {total} signals (session={market_session}). Full synthesis unavailable."
        ),
        "blind_judge_verdict":    majority,
        "blind_judge_confidence": "low",
        "blind_judge_reasoning":  (
            f"Phase 7 timed out — fallback to raw vote consensus "
            f"({n_bull}B/{n_bear}Be/{n_neut}N)."
        ),
        "strongest_evidence":  "",
        "weakest_argument":    "",
    }


def _build_causal_chain(judge: Dict[str, Any], direction: str) -> List[CausalChainStep]:
    """Build 6-step structured causal chain from Judge output."""
    dir_arrow = "[UP]" if direction == "UP" else ("[DOWN]" if direction == "DOWN" else "[NEUTRAL]")

    steps_raw = [
        (1, "TRIGGER EVENT",        judge.get("trigger_event",   "No data — assumed neutral impact")),
        (2, "DIRECT COST IMPACT",   judge.get("cost_impact",     "No data — assumed neutral impact")),
        (3, "DIRECT REVENUE IMPACT",judge.get("revenue_impact",  "No data — assumed neutral impact")),
        (4, "DEMAND SIGNAL",        judge.get("demand_signal",   "No data — assumed neutral impact")),
        (5, "SENTIMENT SIGNAL",     judge.get("sentiment_signal","No data — assumed neutral impact")),
        (6, "NET CONCLUSION",       f"Overall direction: {direction} {dir_arrow} based on cost/revenue/demand/sentiment analysis"),
    ]

    # Reject predictions where more than 2 slots say "No data"
    no_data_count = sum(1 for _, _, val in steps_raw if "No data" in val)
    if no_data_count > 2:
        logger.warning("Causal chain has %d 'No data' slots — Judge may have failed", no_data_count)

    return [
        CausalChainStep(step=step, event=label, consequence=value)
        for step, label, value in steps_raw
    ]


def _build_key_signals(
    market_ctx: Dict[str, Any],
    n_bull: int, n_bear: int, n_neut: int,
    direction: str
) -> List[KeySignal]:
    """Build key signals from live market data."""
    signals = []

    # Iron ore signal
    iron_chg = market_ctx.get("iron_ore_change_pct", 0)
    signals.append(KeySignal(
        signal_type=SignalType.COMMODITY,
        description=f"Iron Ore 62% Fe: ${market_ctx.get('iron_ore_price', 'N/A')}/t | {'+' if iron_chg >= 0 else ''}{iron_chg}% today",
        impact="positive" if iron_chg > 0 else ("negative" if iron_chg < 0 else "neutral"),
        confidence=0.85,
    ))

    # AUD/USD signal
    audusd_chg = market_ctx.get("audusd_change_pct", 0)
    signals.append(KeySignal(
        signal_type=SignalType.MACRO,
        description=f"AUD/USD: {market_ctx.get('audusd_rate', 'N/A')} | {'+' if audusd_chg >= 0 else ''}{audusd_chg}% (USD-denominated revenue impact)",
        impact="negative" if audusd_chg > 0 else ("positive" if audusd_chg < 0 else "neutral"),
        confidence=0.80,
    ))

    # Technical signal — rsi may be None when AV key unavailable
    rsi  = market_ctx.get("ticker_rsi")   # None when unavailable
    macd = market_ctx.get("ticker_macd_signal", "NEUTRAL")
    tech_impact = "neutral"
    if rsi is not None and rsi < 30 or macd == "BULLISH_CROSS":
        tech_impact = "positive"
    elif rsi is not None and rsi > 70 or macd == "BEARISH_CROSS":
        tech_impact = "negative"
    rsi_display = f"RSI(14)={rsi}" if rsi is not None else "RSI=UNAVAILABLE"
    signals.append(KeySignal(
        signal_type=SignalType.MARKET_SENTIMENT,
        description=f"Technicals: {rsi_display} | MACD={macd} | Volume vs Avg={market_ctx.get('ticker_volume_vs_avg', 1.0)}x",
        impact=tech_impact,
        confidence=0.70,
    ))

    # Agent consensus signal
    total = n_bull + n_bear + n_neut
    dom_pct = max(n_bull, n_bear) / total * 100 if total > 0 else 0
    signals.append(KeySignal(
        signal_type=SignalType.MARKET_SENTIMENT,
        description=f"Agent consensus: {n_bull} bullish | {n_bear} bearish | {n_neut} neutral (dominant side: {dom_pct:.0f}%)",
        impact="positive" if direction == "UP" else ("negative" if direction == "DOWN" else "neutral"),
        confidence=round(abs(n_bull - n_bear) / total, 2) if total > 0 else 0.5,
    ))

    return signals


