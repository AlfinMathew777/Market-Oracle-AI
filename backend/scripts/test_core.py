def get_neutral_agent_bias_instruction(trend_label: str) -> str:
    """
    Returns a trend-aware default assumption for neutral fundamentals agents.
    In a downtrend, burden of proof shifts to the bulls — uncertainty = bearish.
    In an uptrend, burden of proof shifts to the bears — uncertainty = bullish.
    """
    if trend_label == "STRONG_DOWNTREND":
        return (
            "IMPORTANT: This stock is in a confirmed STRONG DOWNTREND "
            "(down 8%+ this month, consecutive down days). "
            "In a downtrend, the burden of proof is on the bulls. "
            "If you cannot find a SPECIFIC, MEASURABLE bullish catalyst "
            "(not vague sentiment, not currency moves, not volume), "
            "your default vote must be BEARISH, not neutral. "
            "Uncertainty in a downtrend = bearish."
        )
    elif trend_label == "DOWNTREND":
        return (
            "NOTE: This stock is in a downtrend. "
            "If signals are mixed or unclear, lean bearish rather than neutral. "
            "Require specific evidence to vote bullish."
        )
    elif trend_label == "STRONG_UPTREND":
        return (
            "IMPORTANT: This stock is in a confirmed STRONG UPTREND. "
            "In an uptrend, the burden of proof is on the bears. "
            "If you cannot find a SPECIFIC, MEASURABLE bearish catalyst, "
            "your default vote must be BULLISH, not neutral."
        )
    elif trend_label == "UPTREND":
        return (
            "NOTE: This stock is in an uptrend. "
            "If signals are mixed or unclear, lean bullish rather than neutral."
        )
    else:
        return "Be balanced — consider both upside and downside equally."


