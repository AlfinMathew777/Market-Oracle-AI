    async def _run_judge(
        self,
        ticker: str,
        n_bull: int, n_bear: int, n_neut: int,
        sample_bull: list, sample_bear: list,
        sample_quant: list, sample_fund: list,
        context_block: str,
        lessons_str: str,
        event_context: str,
        has_news: bool = True,
        market_session: str = "UNKNOWN",
        top_critical_news: str = "None",
        axjo_change_pct: Optional[float] = None,
        spx_change_pct: Optional[float] = None,
    ) -> Dict[str, Any]:
        """
        Two-stage Judge pipeline — prevents vote-count anchoring.

        Call 1 (Blind Judge): sees arguments only, NO vote tally.
        Call 2 (Reconciler):  sees Blind verdict + vote tally → final JSON.
        """

        def _fmt_reasons(votes: list) -> str:
            return "\n".join(f"  - {v['reason']}" for v in votes) if votes else "  (none)"

        lessons_block = f"=== LESSONS FROM PAST PREDICTIONS ===\n{lessons_str}\n=== END LESSONS ==="

        no_catalyst_note = ""
        if not has_news:
            no_catalyst_note = (
                "\n=== NOTE: NO NEWS CATALYST DETECTED ===\n"
                "This is a macro or technical-driven session. "
                "Weight the Quant signals and broad market direction more heavily than geopolitical signals.\n"
                "=== END NOTE ===\n"
            )

        # ── Call 1: Blind Judge — arguments only, no vote tally ───────────────
        blind_user = (
            f"{context_block}\n"
            f"{no_catalyst_note}\n"
            f"=== GEOPOLITICAL EVENT ===\n{event_context}\n=== END EVENT ===\n\n"
            f"=== ARGUMENT SUMMARIES (evaluate logic, not vote counts) ===\n\n"
            f"BULL ARGUMENTS:\n{_fmt_reasons(sample_bull)}\n\n"
            f"BEAR ARGUMENTS:\n{_fmt_reasons(sample_bear)}\n\n"
            f"QUANT SIGNALS:\n{_fmt_reasons(sample_quant)}\n\n"
            f"FUNDAMENTAL ANALYSTS:\n{_fmt_reasons(sample_fund)}\n"
            f"=== END ARGUMENTS ===\n\n"
            f"Evaluate argument quality only. Return three lines as instructed."
        )

        blind_system = _blind_judge_prompt(ticker, lessons_block)

        logic_verdict       = "neutral"
        logic_confidence    = "low"
        blind_reasoning     = "Blind Judge unavailable."
        strongest_evidence  = ""
        weakest_argument    = ""

        try:
            blind_resp = await self.llm_router.call_primary(
                system_message=blind_system,
                user_prompt=blind_user,
                session_id=f"{self.simulation_id}_blind_judge",
            )
            for line in blind_resp.splitlines():
                line_u = line.strip().upper()
                if line_u.startswith("LOGIC_VERDICT:"):
                    val = line.split(":", 1)[1].strip().lower()
                    if val in ("bullish", "bearish", "neutral"):
                        logic_verdict = val
                elif line_u.startswith("LOGIC_CONFIDENCE:"):
                    val = line.split(":", 1)[1].strip().lower()
                    if val in ("high", "medium", "low"):
                        logic_confidence = val
                elif line_u.startswith("STRONGEST_EVIDENCE:"):
                    strongest_evidence = line.split(":", 1)[1].strip()[:400]
                elif line_u.startswith("WEAKEST_ARGUMENT:"):
                    weakest_argument = line.split(":", 1)[1].strip()[:400]
                elif line_u.startswith("REASONING:"):
                    blind_reasoning = line.split(":", 1)[1].strip()[:400]
            logger.info("Blind Judge: verdict=%s confidence=%s", logic_verdict, logic_confidence)
        except Exception as e:
            logger.warning("Blind Judge call failed: %s — using neutral fallback", e)

        # ── Call 2: Reconciler — verdict + vote tally → full causal chain JSON ─
        reconciler_system = _reconciler_prompt(
            ticker=ticker,
            logic_verdict=logic_verdict,
            logic_confidence=logic_confidence,
            n_bull=n_bull,
            n_bear=n_bear,
            n_neut=n_neut,
            market_session=market_session,
            top_critical_news=top_critical_news,
            axjo_change_pct=axjo_change_pct,
            spx_change_pct=spx_change_pct,
            lessons_block=lessons_block,
        )

        reconciler_user = (
            f"{context_block}\n\n"
            f"=== GEOPOLITICAL EVENT ===\n{event_context}\n=== END EVENT ===\n\n"
            f"Blind Judge reasoning summary: {blind_reasoning}\n\n"
            f"Apply reconciliation rules and return JSON verdict."
        )

        defaults = {
            "verdict":             logic_verdict,
            "confidence_modifier": 0,
            "override_flag":       None,
            "reasoning":           blind_reasoning or "Judge unavailable — using raw vote consensus.",
            "trigger_event":       "No data — assumed neutral impact",
            "cost_impact":         "No data — assumed neutral impact",
            "revenue_impact":      "No data — assumed neutral impact",
            "demand_signal":       "No data — assumed neutral impact",
            "sentiment_signal":    "No data — assumed neutral impact",
        }

        try:
            rec_resp = await self.llm_router.call_primary(
                system_message=reconciler_system,
                user_prompt=reconciler_user,
                session_id=f"{self.simulation_id}_reconciler",
            )
            parsed = parse_json_response(rec_resp)
            # Clamp confidence_modifier to +10 / 0 / -10
            mod = int(parsed.get("confidence_modifier", 0))
            parsed["confidence_modifier"] = 10 if mod > 5 else (-10 if mod < -5 else 0)
            for k, v in defaults.items():
                if k not in parsed:
                    parsed[k] = v
            # Attach blind judge fields for downstream use
            parsed["blind_judge_verdict"]    = logic_verdict
            parsed["blind_judge_confidence"] = logic_confidence
            parsed["blind_judge_reasoning"]  = blind_reasoning
            parsed["strongest_evidence"]     = strongest_evidence
            parsed["weakest_argument"]       = weakest_argument
            logger.info(
                "Reconciler: verdict=%s modifier=%+d override=%s",
                parsed.get("verdict"), parsed.get("confidence_modifier"), parsed.get("override_flag"),
            )
            return parsed
        except Exception as e:
            logger.warning("Reconciler call failed: %s — using Blind Judge defaults", e)
            defaults["blind_judge_verdict"]    = logic_verdict
            defaults["blind_judge_confidence"] = logic_confidence
            defaults["blind_judge_reasoning"]  = blind_reasoning
            defaults["strongest_evidence"]     = strongest_evidence
            defaults["weakest_argument"]       = weakest_argument
            return defaults

    async def generate_prediction_report(self, simulation_results: Dict[str, Any]) -> PredictionCard:
        """
        Convert simulation_results into a validated PredictionCard.

        Confidence is taken from programmatic calculation — NOT from LLM.
        Causal chain comes from Judge — NOT from generic LLM.
        """
        ticker       = simulation_results["ticker"]
